//  2   executando soma entre dois numeros
#include <iostream>
int main() {
// defini os numeros para a soma
int num1=10;
int num2=5;

// Ralizando a  Operação
int soma=  num1 + num2;
int subtracao= num1 - num2;
// Imprimindo a soma

printf("soma: %d + %d = %d\n", num1,num2,soma);
printf("subtracao: %d - %d = %d\n", num1,num2,subtracao);

return 0;







}




